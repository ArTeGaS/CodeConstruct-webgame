"""Результат: програма має вивести найбільше з двох чисел."""

a = int(input("Введи a: "))
b = int(input("Введи b: "))

if a > b:
    print("Найбільше:", a)
elif a == b:
    print("Числа рівні")
else:
    print("Найбільше:", b)

